    private void btnSiguienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSiguienteActionPerformed

        //Iterator<Auto> Siguiente= listaAutos.iterator();
        //txtMotor.setText(Siguiente.next().getMotor());
        Auto actual = (Auto) listaAutos.get(contador);

        txtMotor.setText(actual.getMotor());
        txtPlaca.setText(actual.getPlaca());
        comboMarca.setSelectedItem(actual.getMarca());
        txtCilindraje.setText(String.valueOf(actual.getCilindraje()));
        spinPuertas.setValue(actual.getPuertas());
        txtModelo.setText(String.valueOf(actual.getModelo()));

        if (contador < listaAutos.size()-1) {
            contador++;

        } else {
            JOptionPane.showMessageDialog(this, "No hay autos siguientes a este");
        }

    }//GEN-LAST:event_btnSiguienteActionPerformed

    public void Limpiar() {
        txtMotor.setText("");
        txtPlaca.setText("");
        comboMarca.setSelectedIndex(0);
        txtCilindraje.setText("");
        spinPuertas.setValue(0);
        txtModelo.setText("");
    }

