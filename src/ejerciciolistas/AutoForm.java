    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String Motor = txtMotor.getText();
        String Placa = txtPlaca.getText();
        String Marca = comboMarca.getSelectedItem().toString();
        double Cilindraje = Double.parseDouble(txtCilindraje.getText());
        int Puertas = Integer.parseInt(spinPuertas.getValue().toString());
        int Modelo = Integer.parseInt(txtModelo.getText());

        Auto objauto = new Auto(Motor, Placa, Marca, Cilindraje, Puertas, Modelo);
        
        listaAutos.Encolar(objauto);

        JOptionPane.showMessageDialog(this, "El Auto fue añadido");

        Limpiar();
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String PlacaBuscar = JOptionPane.showInputDialog("Digite la Placa que quiera Buscar");
        boolean AutoExiste = false;

        for (int i = 0; i < listaAutos.size(); i++) {
            Auto autotemp = (Auto)listaAutos.get(i);

            if (PlacaBuscar.equals(autotemp.getPlaca())) {
                txtMotor.setText(autotemp.getMotor());
                txtPlaca.setText(autotemp.getPlaca());
                comboMarca.setSelectedItem(autotemp.getMarca());
                txtCilindraje.setText(String.valueOf(autotemp.getCilindraje()));
                spinPuertas.setValue(autotemp.getPuertas());
                txtModelo.setText(String.valueOf(autotemp.getModelo()));

                AutoExiste = true;
            }

        }
        if (!AutoExiste) {
            JOptionPane.showMessageDialog(this, "No se encontro el Auto");
    }//GEN-LAST:event_btnBuscarActionPerformed
    }

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        
        listaAutos.Desencolar();
        contador=0;
        JOptionPane.showMessageDialog(this, "El Auto desapilado");
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnAnteriorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAnteriorActionPerformed

        Auto actual = (Auto) listaAutos.get(contador);

        txtMotor.setText(actual.getMotor());
        txtPlaca.setText(actual.getPlaca());
        comboMarca.setSelectedItem(actual.getMarca());
        txtCilindraje.setText(String.valueOf(actual.getCilindraje()));
        spinPuertas.setValue(actual.getPuertas());
        txtModelo.setText(String.valueOf(actual.getModelo()));

        if (contador > 0) {
            contador--;
    }//GEN-LAST:event_btnAnteriorActionPerformed
       else {
            JOptionPane.showMessageDialog(this, "No hay autos anteriores a este");
        }
    }
